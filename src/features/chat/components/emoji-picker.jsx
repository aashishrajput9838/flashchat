export function EmojiPicker({ onEmojiSelect, isVisible, onClose }) {
  const pickerRef = useRef(null);

  useEffect(() => {
    const handleClickOutside = (event) => {
      if (pickerRef.current && !pickerRef.current.contains(event.target)) {
        onClose();
      }
    };

    if (isVisible) {
      document.addEventListener('mousedown', handleClickOutside);
    }

    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
    };
  }, [isVisible, onClose]);

  if (!isVisible) return null;

  return (
    <div 
      ref={pickerRef}
      className="absolute bottom-full left-0 mb-2 w-64 h-48 bg-card border rounded-lg shadow-lg z-50 overflow-hidden"
    >
      <div className="grid grid-cols-10 gap-1 p-2 h-full overflow-y-auto">
        {EMOJIS.map((emoji, index) => (
          <button
            key={index}
            onClick={() => {
              onEmojiSelect(emoji);
              onClose();
            }}
            className="text-2xl p-1 hover:bg-muted rounded transition-colors flex items-center justify-center"
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  );
}